a = 10
b = 3
print("Suma: ", a + b)
print("Resta: ", a - b)
print("Multiplicación: ", a * b)
print("División: ", a / b)
print("División entera: ", a // b)
print("Módulo: ", a % b)
print("Potencia: ", a ** b)
print("Raíz cuadrada de a: ", a ** 0.5)
print("Raíz cuadrada de b: ", b ** 0.5)
print("Valor absoluto de -10: ", abs(-10))
print("Máximo entre a y b: ", max(a, b))
print("Mínimo entre a y b: ", min(a, b))
print("Redondeo de a: ", round(a))
print("Redondeo de b: ", round(b))

